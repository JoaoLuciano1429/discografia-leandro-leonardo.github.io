const player = document.getElementById("player");

function playTrack(albumKey, trackIndex) {
  const album = albumKey;
  const track = album.tracks[trackIndex];
  const trackPath = `audio/${album.folder}/${track}`;

  player.src = trackPath;
  player.play();
}
