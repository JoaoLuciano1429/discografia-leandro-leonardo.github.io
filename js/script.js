// Fun√ß√£o para tocar a pr√≥xima m√∫sica, considerando o modo de repeti√ß√£o e aleat√≥rio
function playNextTrack() {
  if (!currentAlbum || currentTrackIndex === null) return;

  if (isShuffle) {
    shufflePointer++;
    if (shufflePointer >= shuffledIndexes.length) {
      if (isLoopAll) {
        shuffleArray(shuffledIndexes);
        shufflePointer = 0;
      } else {
        return; // Fim da lista aleat√≥ria
      }
    }
    playTrack(currentAlbum, shuffledIndexes[shufflePointer]);
  } else {
    let nextIndex = currentTrackIndex + 1;
    if (nextIndex >= currentAlbum.tracks.length) {
      if (isLoopAll) {
        nextIndex = 0;
      } else {
        return; // Fim do √°lbum
      }
    }
    playTrack(currentAlbum, nextIndex);
  }
}

// Fun√ß√£o para tocar a m√∫sica anterior, considerando o modo de repeti√ß√£o e aleat√≥rio
function playPreviousTrack() {
  if (!currentAlbum || currentTrackIndex === null) return;

  if (isShuffle) {
    shufflePointer--;
    if (shufflePointer < 0) {
      if (isLoopAll) {
        shufflePointer = shuffledIndexes.length - 1;
      } else {
        return;
      }
    }
    playTrack(currentAlbum, shuffledIndexes[shufflePointer]);
  } else {
    let prevIndex = currentTrackIndex - 1;
    if (prevIndex < 0) {
      if (isLoopAll) {
        prevIndex = currentAlbum.tracks.length - 1;
      } else {
        return;
      }
    }
    playTrack(currentAlbum, prevIndex);
  }
}

